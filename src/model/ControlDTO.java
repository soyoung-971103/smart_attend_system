package model;

public class ControlDTO {
	
	int id;
	byte subjecttime;
	byte lecturetime;
	byte mylecturetime;
	byte attendtime;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public byte getSubjecttime() {
		return subjecttime;
	}
	public void setSubjecttime(byte subjecttime) {
		this.subjecttime = subjecttime;
	}
	public byte getLecturetime() {
		return lecturetime;
	}
	public void setLecturetime(byte lecturetime) {
		this.lecturetime = lecturetime;
	}
	public byte getMylecturetime() {
		return mylecturetime;
	}
	public void setMylecturetime(byte mylecturetime) {
		this.mylecturetime = mylecturetime;
	}
	public byte getAttendtime() {
		return attendtime;
	}
	public void setAttendtime(byte attedntime) {
		this.attendtime = attedntime;
	}
	
	

}
